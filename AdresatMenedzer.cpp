int AdresatMenedzer::dodajAdresata(vector <Adresat> &adresaci, int idZalogowanegoUzytkownika, int idOstatniegoAdresata)
{
    Adresat adresat;

    system("cls");
    cout << " >>> DODAWANIE NOWEGO ADRESATA <<<" << endl << endl;
    adresat = podajDaneNowegoAdresata(idZalogowanegoUzytkownika, idOstatniegoAdresata);

    adresaci.push_back(adresat);
    adresat.PlikZAdresatami::dopiszAdresataDoPliku();

    return ++idOstatniegoAdresata;
}

Adresat AdresatMenedzer::podajDaneNowegoAdresata(int idZalogowanegoUzytkownika, int idOstatniegoAdresata)
{
    Adresat adresat;

    adresat.ustawId(++idOstatniegoAdresata);
    adresat.ustawIdUzytkownika(idZalogowanegoUzytkownika);

    cout << "Podaj imie: ";
    adresat.ustawImie( wczytajLinie() );
    adresat.ustawImie( zamienPierwszaLitereNaDuzaAPozostaleNaMale(adresat.imie) );

    cout << "Podaj nazwisko: ";
    adresat.ustawNazwisko( wczytajLinie() );
    adresat.ustawNazwisko( zamienPierwszaLitereNaDuzaAPozostaleNaMale(adresat.nazwisko) );

    cout << "Podaj numer telefonu: ";
    adresat.ustawNumerTelefonu( wczytajLinie() );

    cout << "Podaj email: ";
    adresat.ustawEmail( wczytajLinie() );

    cout << "Podaj adres: ";
    adresat.ustawAdres( wczytajLinie() );

    return adresat;
}

string AdresatMenedzer::zamienPierwszaLitereNaDuzaAPozostaleNaMale(string tekst)
{
    if (!tekst.empty())
    {
        transform(tekst.begin(), tekst.end(), tekst.begin(), ::tolower);
        tekst[0] = toupper(tekst[0]);
    }
    return tekst;
}

